package com.programmingPratice.pepcoading;

public class powerLinear {
    public static void main(String[] args) {

        int a = 2;
        int b = 5;
        int total = 1;
        for (int i = 1; i <= b; i++) {

            total = total * a;

            System.out.println(total);
        }
    }
}
